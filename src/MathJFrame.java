    /**
     *  convert a decimal number to a binary number
     * @param num
     * @return string
     */
    private String decimalToBinary(int num) {
        String result = "";
        
        while(num > 0 ) 
        {
            
           
           //get the remainder
            result = num % 2  + result;
            
            // Divide the positive decimal number by two( then divide the quotient by two)
            num = num /2;
        }
        
        return result;
    }
    
    /** 
     * convert a bianry number to a decimal number
     * @param revStr
     * @return int
     */
    private int binaryToDecimal(String revStr){
        String tempStr ;
        int tempInt;
        int result = 0;
        
        for(int i =0; i < revStr.length(); i++)
        {
            tempStr = String.valueOf(revStr.charAt(i));
            tempInt = Integer.parseInt(tempStr);
            
            //if  the integer number  is not  1 or 0, the number inputed by user is not a bianry number 
            //so return -1;
            if(tempInt != 1 && tempInt != 0)
            {
                return -1;
            }
            
            //if the integer number is 0, skip this number
            if(tempInt == 0)
            {
                continue;
            }
            
            //if the integer number is 1 (tempInt = 1), calculate the power
            result = result + (int)(tempInt * Math.pow(2.0, (double)i));
        }
        
        return result;
    }
    
    /**
     * reverse string
     * @param origStr
     * @return 
     */
    private String getRevStr(String origStr) {
        String revStr = "";
        for(int i = origStr.length() - 1; i>= 0; i--)
        {
            revStr = revStr + origStr.charAt(i);
                
        }
        
        return revStr;
    }
    
