    /**
     * determine whether a number is a prime or not
     * @param num
     * @return boolean
     */
    private boolean determinePrime(int num) {
        int temp;
        boolean isPrime = true;
        
        if(num <= 1)
        {
            isPrime = false;
        }
        else
        {
            for(int i = 2;i<=num/2; i++)
            {
                //divide the number by i, get the remainder 
                temp = num % i;
                
                //if remainder is 0, the number is not prime
                if(temp == 0)
                {
                    isPrime = false;
                    break;
                }
            }
        }
        
        return isPrime;
    }
