    private void treeMathValueChanged(javax.swing.event.TreeSelectionEvent evt) {//GEN-FIRST:event_treeMathValueChanged
        // TODO add your handling code here:
       Object object = treeMath.getLastSelectedPathComponent();
        if (object == null){
          return;
        }
        CardLayout card = (CardLayout)panContent.getLayout();
        TreeNode node = (TreeNode) object;
        String value = node.toString();
       
        //different node matches diffrent panel
        if(value.equals("Math Tutorial"))
        {
           
            card.show(panContent, "panWelcome");
             return;   
        }
        else if(value.equals("Factorial"))
        {
            card.show(panContent, "panFactorial");
             return;   
        }
        else if(value.equals("Prime"))
        {
            card.show(panContent, "panPrime");
             return;   
        }
        else if(value.equals("Binary and Decimal"))
        {
            card.show(panContent, "panBinAndDec");
             return;   
        }
        
        TreeNode parent = node.getParent();
        String parentValue = parent.toString();
        
       
       if( parentValue.equals("Prime"))
       {
           if(value.equals("test"))
           {
              initPanPrimeTest();
               card.show(panContent,"panPrimeTest");
           }
       }
       else if( parentValue.equals("Factorial"))
       {
           
           if(value.equals("exercise"))
           { 
               //when initial the factorial exercise panel, the answers are not visible.
               txtA1.setVisible(false);
               txtA2.setVisible(false);
               txtA3.setVisible(false);
               txtA4.setVisible(false);
               card.show(panContent,"panFactorialExe");
           }
       }
       else if( parentValue.equals("Binary and Decimal"))
       {
           
           if(value.equals("bin to dec"))
           { 
             
             card.show(panContent,"panBinToDec");
           }
           else if(value.equals("dec to bin"))
           { 
             
             card.show(panContent,"panDecToBin");
           }
           else if(value.equals("converter"))
           { 
             
             card.show(panContent,"panBinAndDecConverter");
           }
           
           
       }
       
      

    }//GEN-LAST:event_treeMathValueChanged

